# ============================================================
# ПЕРЕМЕННЫЕ И КОНСТАНТЫ В PYTHON
# ============================================================

# Переменная создаётся простым присваиванием — без объявления типа.
# Python сам определяет тип по значению (динамическая типизация).

name = "Alice"
age = 25
height = 1.75

# Переименовать переменную или изменить её значение можно в любой момент
age = 26

# Несколько переменных сразу (множественное присваивание)
x, y, z = 1, 2, 3

# Одно значение нескольким переменным
a = b = c = 0


# ============================================================
# КОНСТАНТЫ
# ============================================================

# В Python нет встроенного механизма констант.
# По соглашению (PEP 8) константы пишут ЗАГЛАВНЫМИ_БУКВАМИ.
# Это лишь договорённость — Python не запрещает их изменить.

MAX_CONNECTIONS = 100
PI = 3.14159
APP_NAME = "MyApp"


# ============================================================
# ПРИМИТИВНЫЕ ТИПЫ ДАННЫХ
# ============================================================

# --- int (целое число) ---
count = 42
negative = -7
big_number = 1_000_000      # можно использовать _ для читаемости

# --- float (число с плавающей точкой) ---
price = 9.99
temperature = -3.5
scientific = 1.5e10         # 1.5 × 10^10

# --- complex (комплексное число) ---
c = 2 + 3j                  # j — мнимая единица

# --- bool (булево значение) ---
is_active = True
is_deleted = False
# bool — это подтип int: True == 1, False == 0

# --- str (строка) ---
greeting = "Hello, World!"
multiline = """Это
многострочная
строка"""
char = 'A'                  # отдельного типа char нет — это просто str длиной 1

# --- NoneType (отсутствие значения) ---
result = None               # аналог null/nil в других языках


# ============================================================
# ПРОВЕРКА ТИПА
# ============================================================

print(type(count))          # <class 'int'>
print(type(price))          # <class 'float'>
print(type(is_active))      # <class 'bool'>
print(type(greeting))       # <class 'str'>
print(type(result))         # <class 'NoneType'>

# isinstance() — предпочтительный способ проверки типа
print(isinstance(count, int))       # True
print(isinstance(price, float))     # True


# ============================================================
# ПРИВЕДЕНИЕ ТИПОВ (type casting)
# ============================================================

# str -> int
num = int("42")             # 42

# int -> float
f = float(10)               # 10.0

# float -> int (дробная часть отбрасывается, не округляется)
i = int(3.9)                # 3

# число -> str
s = str(100)                # "100"

# int -> bool (0 = False, всё остальное = True)
print(bool(0))              # False
print(bool(42))             # True
print(bool(""))             # False — пустая строка тоже False
